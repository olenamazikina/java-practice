package day19_while_loops;

public class DoWhileRunOnce {
public static void main(String[] args) {
	//in this example do while loop body code will run //one the test the condition .then exit the loop as conditon false
	int count = 10;
	do {
		System.out.println("Count: "+count);
		count++;
	}while(count<=5);
	}
}

